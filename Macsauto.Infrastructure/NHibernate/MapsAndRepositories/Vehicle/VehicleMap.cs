namespace Macsauto.Infrastructure.NHibernate.MapsAndRepositories.Vehicle
{
    public class VehicleMap : EntityMap<Domain.SalesModule.Vehicle>
    {
        public VehicleMap()
        {
            References(x => x.Model);
            Map(x => x.Color);
            Map(x => x.CurrentKM);
        }
    }
}
