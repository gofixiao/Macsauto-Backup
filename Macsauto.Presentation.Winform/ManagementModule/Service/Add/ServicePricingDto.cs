namespace Macsauto.Presentation.WinForm.ManagementModule.Service
{
    using Macsauto.Domain.SalesModule;

    public class ServicePricingDto
    {
        private VehicleSize _vehicleSize;
        private long _price;

        public ServicePricingDto(VehicleSize vehicleSize, long price)
        {
            _vehicleSize = vehicleSize;
            _price = price;
        }

        public VehicleSize VehicleSize
        {
            get { return _vehicleSize; }
        }

        public long Price
        {
            get { return _price; }
        }
    }
}